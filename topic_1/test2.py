text = "  BMW M5 e34 4.5L Diesel  "

# Test the strip() method
stripped_text = text.strip()  # Removes leading and trailing whitespace
print(f"Strip: '{stripped_text}'")

# Test the capitalize() method
capitalized_text = text.capitalize()  # Capitalizes the first character
print(f"Capitalize: '{capitalized_text}'")

# Test the title() method
title_text = text.title()  # Capitalizes the first letter of each word
print(f"Title: '{title_text}'")

# Test the upper() method
upper_text = text.upper()  # Converts all characters to uppercase
print(f"Upper: '{upper_text}'")

# Test the lower() method
lower_text = text.lower()  # Converts all characters to lowercase
print(f"Lower: '{lower_text}'")
